/*
Bases de Datos:
Es un conjunto de datos almacenados en un sistema de computadora, que pueden
ser consultados y actualizados de manera simultánea por varios usuarios.

Datos:
Son hechos conocidos que pueden ser registrados y que tienen un significado
implícito.

Información:
Es el resultado del procesamiento de los datos.

Sistema de Gestión de Bases de Datos (SGBD):
Es un conjunto de programas que permiten el almacenamiento, modificación y
extracción de la información en una base de datos.

Tipos de Bases de Datos:
- Jerárquicas
- Red
- Relacionales
- Orientadas a Objetos
- NoSQL

Entidades:
Son objetos o conceptos del mundo real que se pueden identificar y describir.

Tipos de Entidades:
- De Datos
- Catálogos
- Pivotes

Atributos:
Son las características de las entidades.

Tipos de Datos(Atributos):
- Numeros enteros y flotantes
- Cadenas de texto
- Fechas
- Booleanos
- Blobs y archivos
- Datos geográficos

Relaciones:
Son las asociaciones entre las entidades.

Modelo Relacional:
Es un modelo de datos que representa la información en forma de tablas.

Tablas:
Son estructuras de datos que almacenan la información en filas y columnas.

Filas:
Son los registros de la tabla.

Columnas:
Son los campos de la tabla.

Clave Primaria:
Es un campo o conjunto de campos que identifican de manera única a cada
registro de la tabla.

Clave Foránea:
Es un campo o conjunto de campos que hacen referencia a la clave primaria de
otra tabla.

SQL (Structured Query Language):
Es un lenguaje de programación que se utiliza para gestionar bases de datos
relacionales.

DDL (Data Definition Language):
Es un subconjunto de SQL que se utiliza para definir la estructura de la base
de datos.

DML (Data Manipulation Language):
Es un subconjunto de SQL que se utiliza para manipular los datos de la base de
datos.

DCL (Data Control Language):
Es un subconjunto de SQL que se utiliza para controlar el acceso a los datos.

TCL (Transaction Control Language):
Es un subconjunto de SQL que se utiliza
para controlar las transacciones en la base de datos.

Comentarios:
Son textos que se utilizan para documentar el código fuente de un programa.

Comentario de una línea:
Se utiliza para documentar una línea de código.

Comentario de varias líneas:
Se utiliza para documentar varias líneas de código.

Comentario de bloque:
Se utiliza para documentar un bloque de código.

Comentario de documentación:
Se utiliza para documentar un programa.

CRUD:
Es un acrónimo que se utiliza para referirse a las operaciones básicas de
manipulación de datos en una base de datos:
- Create
- Read
- Update
- Delete

Logica de Negocio:
Es el conjunto de reglas y procedimientos que definen el comportamiento de un
sistema de información.

Llaves:
Son los campos que se utilizan para relacionar las tablas de una base de datos.

Llave Primaria:
Es un campo o conjunto de campos que identifican de manera única a cada
registro de la tabla.

Llave Foránea:
Es un campo o conjunto de campos que hacen referencia a la clave primaria de
otra tabla.

Llave Unica:
Es un campo o conjunto de campos que identifican de manera única a cada
registro de la tabla.

Llave Candidata:
Es un campo o conjunto de campos que pueden ser utilizados como clave primaria.

Llave Compuesta:
Es un conjunto de campos que se utilizan como clave primaria.

Llave Natural:
Es un campo o conjunto de campos que se utilizan como clave primaria y que
tienen un significado en el mundo real.

Llave Sintética:
Es un campo o conjunto de campos que se utilizan como clave primaria y que no
tienen un significado en el mundo real.

Relaciones:
Son las asociaciones entre las entidades.

Relación Uno a Uno:
Es una relación en la que un registro de una tabla se relaciona con un solo
registro de otra tabla.

Relación Uno a Muchos:
Es una relación en la que un registro de una tabla se relaciona con varios
registros de otra tabla.

Relación Muchos a Muchos:
Es una relación en la que varios registros de una tabla se relacionan con
varios registros de otra tabla.

Modelo Entidad-Relación:
Es un modelo de datos que representa la información en forma de entidades y
relaciones.

Modelo Relacional:
Es un modelo de datos que representa la información en forma de tablas.

Normalización:
Es el proceso de organizar los datos en una base de datos relacional.

Normalización:
Es el proceso de organizar los datos en una base de datos relacional.

Formas Normales:
Son reglas que se utilizan para organizar los datos en una base de datos
relacional.

Primera Forma Normal (1FN):
Es una forma normal que se utiliza para organizar los datos en una base de
datos relacional.

Segunda Forma Normal (2FN):
Es una forma normal que se utiliza para organizar los datos en una base de
datos relacional.

Tercera Forma Normal (3FN):
Es una forma normal que se utiliza para organizar los datos en una base de
datos relacional.

Cuarta Forma Normal (4FN o Boyce-Codd Normal Form - BCNF):
Es una forma normal que se utiliza para organizar los datos en una base de
datos relacional.

Quinta Forma Normal (5FN o Domain-Key Normal Form - DKNF):
Es una forma normal que se utiliza para organizar los datos en una base de
datos relacional.
*/