Tipos de datos:
Cada columna de una tabla de base de datos debe tener un nombre y un tipo de datos.

Un desarrollador de SQL debe decidir qué tipo de datos se almacenarán dentro de cada
columna al crear una tabla. El tipo de datos es una guía para que SQL comprenda qué
tipo de datos se espera dentro de cada columna y también identifica cómo interactuará
SQL con los datos almacenados.
*/