Tipos de datos:
Cada columna de una tabla de base de datos debe tener un nombre y un tipo de datos.

Un desarrollador de SQL debe decidir qué tipo de datos se almacenarán dentro de cada
columna al crear una tabla. El tipo de datos es una guía para que SQL comprenda qué
tipo de datos se espera dentro de cada columna y también identifica cómo interactuará
SQL con los datos almacenados.

En SQL (Structured Query Language), los tipos de datos son utilizados para definir
el tipo de valor que puede almacenarse en una columna de una tabla o una variable
en un programa. Los tipos de datos en SQL pueden variar dependiendo del sistema de
gestión de bases de datos (DBMS) específico que estés utilizando, pero hay algunos
tipos de datos comunes que se encuentran en la mayoría de los sistemas.
Aquí hay una lista de algunos tipos de datos comunes en SQL:

INTEGER/INT: Para almacenar números enteros, como 1, 2, -5, etc.

DECIMAL/NUMERIC: Para almacenar números decimales o de punto flotante con una
precisión específica, como 3.14, 0.001, etc.

CHAR(n): Para almacenar una cadena de caracteres de longitud fija, donde 'n'
especifica la longitud máxima de la cadena.

VARCHAR(n): Para almacenar una cadena de caracteres de longitud variable, donde 'n'
especifica la longitud máxima de la cadena.

DATE: Para almacenar fechas en el formato 'YYYY-MM-DD'.

TIME: Para almacenar una hora del día en formato 'HH:MM:SS'.

DATETIME/TIMESTAMP: Para almacenar tanto fecha como hora en formato
'YYYY-MM-DD HH:MM:SS'.

BOOLEAN/BOOL: Para almacenar valores booleanos (TRUE o FALSE).

BLOB: Para almacenar grandes bloques de datos binarios, como imágenes, archivos, etc.

CLOB: Para almacenar grandes bloques de datos de caracteres, como texto largo,
documentos, etc.

Estos son solo algunos ejemplos de tipos de datos comunes en SQL. Los DBMS también
pueden proporcionar tipos de datos adicionales específicos de sus plataformas.
Es importante consultar la documentación del sistema de gestión de bases de datos
que estés utilizando para obtener una lista completa de tipos de datos y sus
características específicas.
*/